
export default function checkForTriplets () {
    let a = prompt("enter hyp value");
    let b = prompt("enter opp value");
    let c = prompt("enter adj value");
    let hyp = a * a;
    let opp = b * b;
    let adj = c * c;
    if (hyp = opp + adj) {
        alert("yes!");
    }
    else {
        alert("no!");
    }
};











